                            <h3>Importancia de la Geografía Humana</h3>
                            <p>Entendemos por geografía humana a una de las ramas de la geografía que tiene por objetivo el estudio y análisis de las relaciones que se establecen
                              entre el ser humano y el medio ambiente en el que vive y al cual transforma permanentemente.
                            </p>
                            <p>
                              La geografía humana es una de las menos conocidas en términos generales pero una de las más importantes para entender los fenómenos que 
                              ocurren en el planeta que vivimos y que muchas veces son consecuencia de la acción del hombre.
                            </p>
                            <p>La geografía humana es específica y tiene como interés sólo el análisis de aquellos fenómenos o realidades en los que participa el ser humano y que se dan en un entorno geográfico. 
                              Así, la disciplina sirve para comprender las relaciones que se establecen entre las dos partes y los posibles lazos de transformación que una le puede generar a la otra. 
                              Si bien en la Antigüedad la fuerza del entorno y del ambiente en la calidad de vida del ser humano era mucho más fuerte e incluso incontrolable, 
                              hoy en día esa relación de fuerzas se ha invertido y es el ser humano quien en la mayoría de los casos altera la naturaleza que lo rodea, 
                              generando en ocasiones cambios indelebles e incontrolables como el famoso y peligrosísimo cambio climático.
                            </p>
         
                        </div>
                        <div id="sidebar2">
                          <h3>Importancia del Espacio Geográfico</h3>
                          <p>
                            El espacio geográfico es un concepto propio de la geografía y que 
                            se enfoca en tratar al territorio desde la perspectiva de las consideraciones que la sociedad tiene del mismo,
                            o desde la perspectiva de las actividades que allí se llevan a cabo.
                          </p>
                          <p>
                            En otras palabras, el espacio geográfico alude al territorio socialmente considerado ya sea por cuestiones políticas, económicas o meramente vivenciales. 
                            El concepto es relevante porque pone en énfasis la relación existente entre las comunidades y los lugares en las que las mismas se desarrollan. 
                            Continuamente hacemos referencia a lugares no solo desde la perspectiva de la ubicación, 
                            sino también desde el sentido que la sociedad les otorga.
                          </p>
                          <p>
                            Un aspecto fundamental del espacio geográfico es el de las actividades económicas. En efecto, las sociedades utilizan el medio ambiente para el desarrollo de distintas actividades productivas, 
                            actividades para las que suelen tener algún tipo de ventaja. Este tipo de actividad va modificando paulatinamente el paisaje de diversas formas. 
                            De esta manera, es posible hacer referencia a un ámbito rural, a un ámbito urbano, a un ámbito industrial, etc. Si lo pensamos detenidamente, 
                            existe una relación estrecha entre esta modificación y las ventajas que una zona ofrece con respecto a alguna actividad específica de índole productiva.
                          </p>
