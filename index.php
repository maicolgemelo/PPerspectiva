                   <h2>Perspectiva:</h2>
                    <p>
                     El término perspectiva (en latín, perspicere "para ver a través de")1​ se utiliza en las artes gráficas para designar a una representación,
                     generalmente sobre una superficie plana (como el papel o un lienzo), de un motivo tal como es percibido por la vista, de forma que se pueda
                     intuir su configuración tridimensional.
                    </p>
                    <p>
                     Geométricamente, estas representaciones se obtienen a partir de la intersección de un plano [el plano del dibujo] con un conjunto de visuales
                     (las líneas rectas o rayos que unen los puntos del objeto representado con el punto desde el que se observa (denominado el punto de vista)).
                    </p>
                    </div>
                    <div id="conten2">
                      <div id="parraf3">
                        <p>
                            El término perspectiva es usado en múltiples ocasiones, y en la mayoría de esas ocasiones,
                             se refiere el concepto a cuestiones diversas, por lo cual intentaremos poner un poco de claridad a cuáles usos puede darse a este término 
                             y cómo es usado en diferentes situaciones y contextos.
                        </p>

                      </div>
                      <div id="parraf4">
                        <p>
                            la perspectiva es una técnica por la cual se pueden representar objetos en tercera dimensión pero en una superficie que es plana,
                            como por ejemplo un papel o una tela (lienzos). A partir de esa perspectiva de tercera dimensión, al objeto se le atribuye una sensación de profundidad y de volumen. 
                            Es utilizada de este modo la perspectiva cuando necesitamos dibujar cubos por ejemplo, y en arquitectura se utiliza mucho cuando se deben realizar representaciones de obras en tercera dimensión 
                            (en la actualidad, mucho puede contribuir a esto la utilización de programas informáticos que facilitan la tarea de dibujar).
                        </p>

